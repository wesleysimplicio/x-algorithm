def test_main_check_after_write_is_clean(tmp_path: Path, monkeypatch, capsys):
    """Write artifacts, then --check should pass."""
    # Run main without --check to write.
    rc_write = ga.main([])
    assert rc_write == 0
    # Now --check should be clean.
    rc_check = ga.main(["--check"])
    assert rc_check == 0
    out = capsys.readouterr().out
    assert "OK" in out or "in sync" in out
