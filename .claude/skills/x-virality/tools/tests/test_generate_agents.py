def test_list_skill_files_excludes_tests():
    files = ga.list_skill_files(SKILL_DIR)
    for f in files:
        assert "/tests/" not in str(f), f"tests/ should be excluded: {f}"


def test_list_skill_files_includes_skill_md():
    files = ga.list_skill_files(SKILL_DIR)
    names = {f.name for f in files}
    assert "SKILL.md" in names


def test_list_skill_files_includes_recipes():
    files = ga.list_skill_files(SKILL_DIR)
    rel_strs = {str(f.relative_to(SKILL_DIR)) for f in files}
    # at least the reply-bait recipe ships
    assert any("recipes/reply-bait.md" in s for s in rel_strs)


