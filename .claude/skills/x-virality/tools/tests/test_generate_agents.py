"""Tests for the AGENTS.md / tota.skill.json generator."""
from __future__ import annotations

import json
from pathlib import Path

import pytest

import generate_agents as ga


SKILL_DIR = Path(__file__).resolve().parent.parent.parent


# ---------------------------------------------------------------------------
# parse_frontmatter
# ---------------------------------------------------------------------------


def test_parse_frontmatter_on_live_skill():
    fm = ga.parse_frontmatter(SKILL_DIR / "SKILL.md")
    assert fm.name == "x-virality"
    assert "X" in fm.description or "x" in fm.description.lower()
    assert len(fm.description) > 50  # description is substantive


def test_parse_frontmatter_missing_raises(tmp_path: Path):
    md = tmp_path / "no-frontmatter.md"
    md.write_text("# Just a heading\n")
    with pytest.raises(RuntimeError, match="No YAML frontmatter"):
        ga.parse_frontmatter(md)


def test_parse_frontmatter_missing_name_raises(tmp_path: Path):
    md = tmp_path / "no-name.md"
    md.write_text("---\ndescription: foo\n---\n")
    with pytest.raises(RuntimeError, match="No `name:`"):
        ga.parse_frontmatter(md)


def test_parse_frontmatter_missing_description_raises(tmp_path: Path):
    md = tmp_path / "no-desc.md"
    md.write_text("---\nname: x\n---\n")
    with pytest.raises(RuntimeError, match="No `description:`"):
        ga.parse_frontmatter(md)


def test_parse_frontmatter_collapses_whitespace(tmp_path: Path):
    md = tmp_path / "multiline.md"
    md.write_text(
        "---\n"
        "name: skill\n"
        "description: First line\n"
        "  second line\n"
        "  third line.\n"
        "---\n"
        "body\n"
    )
    fm = ga.parse_frontmatter(md)
    assert fm.description == "First line second line third line."


